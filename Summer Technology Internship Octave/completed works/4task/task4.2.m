clc;
clear;

disp('ЗАДАНИЕ 2. Решение СЛАУ');
disp('Вариант 7');

% Система:
% 2*x1 + x2 + 6*x3 = 31
% x1 + x2 - 2*x3 = 7
% -x1 - 3*x2 + 2*x3 = -13

A = [ 2   1   6;
      1   1  -2;
     -1  -3   2 ];

b = [31; 7; -13];

disp('Матрица коэффициентов A:');
disp(A);

disp('Вектор свободных членов b:');
disp(b);

disp('Определитель матрицы A:');
D = det(A);
disp(D);

% ==========================================================
% 1. Метод Крамера
% ==========================================================

disp(' ');
disp('1. Метод Крамера');

A1 = A;
A1(:, 1) = b;

A2 = A;
A2(:, 2) = b;

A3 = A;
A3(:, 3) = b;

D1 = det(A1);
D2 = det(A2);
D3 = det(A3);

x_cramer = [D1 / D;
            D2 / D;
            D3 / D];

disp('D = ');
disp(D);

disp('D1 = ');
disp(D1);

disp('D2 = ');
disp(D2);

disp('D3 = ');
disp(D3);

disp('Решение методом Крамера:');
disp(x_cramer);

% ==========================================================
% 2. Метод обратной матрицы
% ==========================================================

disp(' ');
disp('2. Метод обратной матрицы');

x_inverse = inv(A) * b;

disp('Решение методом обратной матрицы:');
disp(x_inverse);

% ==========================================================
% 3. Метод Гаусса
% ==========================================================

disp(' ');
disp('3. Метод Гаусса');

C = rref([A b]);

disp('Расширенная матрица после преобразования:');
disp(C);

x_gauss = C(:, 4);

disp('Решение методом Гаусса:');
disp(x_gauss);

% ==========================================================
% 4. Метод LU-разложения
% ==========================================================

disp(' ');
disp('4. Метод LU-разложения');

[L, U, P] = lu(A);

% Так как P*A = L*U, то нужно решать:
% L*y = P*b
% U*x = y

y_lu = L \ (P * b);
x_lu = U \ y_lu;

disp('Матрица L:');
disp(L);

disp('Матрица U:');
disp(U);

disp('Матрица P:');
disp(P);

disp('Решение методом LU-разложения:');
disp(x_lu);

% ==========================================================
% 5. Метод QR-разложения
% ==========================================================

disp(' ');
disp('5. Метод QR-разложения');

[Q, R] = qr(A);

% Так как A = Q*R, то:
% Q*R*x = b
% R*x = Q'*b

y_qr = Q' * b;
x_qr = R \ y_qr;

disp('Матрица Q:');
disp(Q);

disp('Матрица R:');
disp(R);

disp('Решение методом QR-разложения:');
disp(x_qr);

% ==========================================================
% Проверка
% ==========================================================

disp(' ');
disp('Проверка A*x - b:');

disp('Проверка для метода Крамера:');
disp(A * x_cramer - b);

disp('Проверка для метода обратной матрицы:');
disp(A * x_inverse - b);

disp('Проверка для метода Гаусса:');
disp(A * x_gauss - b);

disp('Проверка для LU-разложения:');
disp(A * x_lu - b);

disp('Проверка для QR-разложения:');
disp(A * x_qr - b);
